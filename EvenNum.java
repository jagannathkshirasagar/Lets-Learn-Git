
    public class EvenNum {
        public static void main(String[] args) {
            System.out.println("Even numbers between 1 and 10 are:");
            
            for (int i = 1; i <= 10; i++) {
                if (i % 2 == 0) {
                    System.out.print(i + " ");
                }
            }
        }
    }

